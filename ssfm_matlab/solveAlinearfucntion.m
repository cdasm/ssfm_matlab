function [x,y,z]=solveAlinearfucntion(A)

tm=num2cell(A);

[ a1_1, a1_2, a1_3, a1_4]=tm{1,:};
[ a2_1, a2_2, a2_3, a2_4]=tm{2,:};
[ a3_1, a3_2, a3_3, a3_4]=tm{3,:};

d=(a1_1*a2_2*a3_3 - a1_1*a2_3*a3_2 - a1_2*a2_1*a3_3 + a1_2*a2_3*a3_1 + a1_3*a2_1*a3_2 - a1_3*a2_2*a3_1);

x=-(a1_2*a2_3*a3_4 - a1_2*a2_4*a3_3 - a1_3*a2_2*a3_4 + a1_3*a2_4*a3_2 + a1_4*a2_2*a3_3 - a1_4*a2_3*a3_2)/d;
y=(a1_1*a2_3*a3_4 - a1_1*a2_4*a3_3 - a1_3*a2_1*a3_4 + a1_3*a2_4*a3_1 + a1_4*a2_1*a3_3 - a1_4*a2_3*a3_1)/d; 
z=-(a1_1*a2_2*a3_4 - a1_1*a2_4*a3_2 - a1_2*a2_1*a3_4 + a1_2*a2_4*a3_1 + a1_4*a2_1*a3_2 - a1_4*a2_2*a3_1)/d;
 

end